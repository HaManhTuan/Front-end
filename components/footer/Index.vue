<script>
export default {
  name: "Index",
  mounted() {
    const svgCircle =
      document.querySelector(".rn-progress-parent .rn-back-circle path") ||
      null;
    window.addEventListener("scroll", () => {
      const scrollToTop = document.querySelector(".rn-progress-parent") || null;
      if (window.screen.width >= 375) {
        if (window.pageYOffset >= 70) {
          if (scrollToTop) {
            scrollToTop.classList.add("rn-backto-top-active");
          }
        } else {
          scrollToTop.classList.remove("rn-backto-top-active");
        }
      }

      if (svgCircle) {
        const totalLength = svgCircle.getTotalLength() || 0;
        const clientHeight = document.documentElement.clientHeight;
        const scrollHeight = document.documentElement.scrollHeight;
        const scrollTop = document.documentElement.scrollTop;
        const percentage = scrollTop / (scrollHeight - clientHeight);
        svgCircle.style.cssText = `
          transition: stroke-dashoffset 10ms linear 0s;
          stroke-dasharray: ${totalLength}, ${totalLength};
          stroke-dashoffset: ${totalLength - totalLength * percentage};
           `;
      }
    });
    const scrollToTopBtn = document.querySelector(".rn-progress-parent");
    scrollToTopBtn.addEventListener("click", () => {
      const rootElement = document.documentElement;
      rootElement.scrollTo({
        top: 0,
        behavior: "smooth",
      });
    });
  },
};
</script>

